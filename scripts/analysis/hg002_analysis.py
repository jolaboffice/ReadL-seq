# ====================================================================
# Interval arithmetic
# ====================================================================

def merge_intervals(ivs: list[tuple]) -> np.ndarray:
    """Merge overlapping ``(start, end)`` pairs into an Nx2 int64 array."""
    if not ivs:
        return np.empty((0, 2), dtype=np.int64)
    s = sorted(ivs)
    merged = [list(s[0])]
    for a, b in s[1:]:
        if a <= merged[-1][1]:
            merged[-1][1] = max(merged[-1][1], b)
        else:
            merged.append([a, b])
    return np.array(merged, dtype=np.int64)


def intervals_bases(ivs: np.ndarray) -> int:
    """Total base-pairs covered by merged intervals."""
    return 0 if len(ivs) == 0 else int(np.sum(ivs[:, 1] - ivs[:, 0]))


def pos_in_intervals(positions: np.ndarray, ivs: np.ndarray) -> np.ndarray:
    """Boolean mask indicating which *positions* fall inside *ivs*."""
    if len(ivs) == 0 or len(positions) == 0:
        return np.zeros(len(positions), dtype=bool)
    idx = np.searchsorted(ivs[:, 0], positions, side="right") - 1
    mask = np.zeros(len(positions), dtype=bool)
    ok = idx >= 0
    mask[ok] = positions[ok] < ivs[idx[ok], 1]
    return mask


