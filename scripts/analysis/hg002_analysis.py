def _gff_contig(name: str) -> str:
    """Convert GFF contig names: ``chr1_MATERNAL`` -> ``chr1_mat``."""
    return name.replace("_MATERNAL", "_mat").replace("_PATERNAL", "_pat")


def _gff_attr(attrs: str, key: str) -> str | None:
    """Extract a single attribute value from a GFF attribute string."""
    for a in attrs.split(";"):
        if a.startswith(key + "="):
            return a.split("=", 1)[1]
    return None


def parse_gff(gff_path: str) -> dict[str, dict[str, np.ndarray]]:
    """Parse a GFF file into per-contig, per-feature merged interval arrays.

    Returns ``{contig: {feature_name: Nx2 int64 array}}`` in 0-based
    half-open coordinates.
    """
    gene_info: dict = {}
    gene_tx: dict[str, list] = defaultdict(list)
    tx_exons: dict[str, list] = defaultdict(list)
    tx_cds: dict[str, list] = defaultdict(list)

    print(f"  Parsing {os.path.basename(gff_path)} ...")
    with gzip.open(gff_path, "rt") as fh:
        for line in fh:
            if line[0] == "#":
                continue
            cols = line.rstrip("\n").split("\t")
            if len(cols) < 9:
                continue
            contig, ft = _gff_contig(cols[0]), cols[2]
            s, e = int(cols[3]) - 1, int(cols[4])  # 1-based -> 0-based
            strand, at = cols[6], cols[8]

            if ft == "gene":
                gid = _gff_attr(at, "ID")
                gene_info[gid] = (contig, s, e, strand,
                                  _gff_attr(at, "gene_biotype"))
            elif ft == "transcript":
                gene_tx[_gff_attr(at, "Parent")].append(
                    _gff_attr(at, "ID"))
            elif ft == "exon":
                tx_exons[_gff_attr(at, "Parent")].append((s, e))
            elif ft == "CDS":
                tx_cds[_gff_attr(at, "Parent")].append((s, e))

    # Assign genomic regions to feature types
    raw: dict = defaultdict(lambda: defaultdict(list))
    for gid, (contig, gs, ge, strand, bio) in gene_info.items():
        coding = bio == "protein_coding"

        # Promoter (upstream of TSS)
        if strand == "+":
            ps, pe = max(0, gs - PROMOTER_SIZE), gs
        else:
            ps, pe = ge, ge + PROMOTER_SIZE
        if pe > ps:
            raw[contig]["Promoter"].append((ps, pe))

        # Gene body initially labelled as Intron; exon-level features
        # will take priority via the classify() priority list.
        raw[contig]["Intron"].append((gs, ge))

        for tid in gene_tx.get(gid, []):
            exons = tx_exons.get(tid, [])
            cdss = tx_cds.get(tid, [])
            if coding and cdss:
                cds_lo = min(c[0] for c in cdss)
                cds_hi = max(c[1] for c in cdss)
                for es, ee in exons:
                    for cs, ce in cdss:
                        ov_s, ov_e = max(cs, es), min(ce, ee)
                        if ov_s < ov_e:
                            raw[contig]["CDS"].append((ov_s, ov_e))
                    if strand == "+":
                        if es < cds_lo:
                            raw[contig]["5'UTR"].append(
                                (es, min(ee, cds_lo)))
                        if ee > cds_hi:
                            raw[contig]["3'UTR"].append(
                                (max(es, cds_hi), ee))
                    else:
                        if ee > cds_hi:
                            raw[contig]["5'UTR"].append(
                                (max(es, cds_hi), ee))
                        if es < cds_lo:
                            raw[contig]["3'UTR"].append(
                                (es, min(ee, cds_lo)))
            else:
                for es, ee in exons:
                    raw[contig]["Exon (ncRNA)"].append((es, ee))

    return {contig: {ft: merge_intervals(raw[contig][ft])
                     for ft in raw[contig]}
            for contig in raw}


# ====================================================================
# Feature classification
# ====================================================================

def classify(positions: np.ndarray,
             feat_contig: dict[str, np.ndarray]) -> np.ndarray:
    """Assign each 0-based position its highest-priority feature label.

    Features later in ``FEATURES`` take priority over earlier ones.
    """
    labels = np.full(len(positions), "Intergenic", dtype="U15")
    for ft in FEATURES:
        if ft == "Intergenic":
            continue
        ivs = feat_contig.get(ft)
        if ivs is not None and len(ivs) > 0:
            labels[pos_in_intervals(positions, ivs)] = ft
    return labels


def build_resolved_segments(
    feat_contig: dict[str, np.ndarray], chrom_len: int,
) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Partition a chromosome into non-overlapping segments, each labelled
    with its highest-priority feature.

    Returns ``(seg_starts, seg_ends, seg_labels)`` — all length-N arrays.
    """
    boundaries = sorted({0, chrom_len} | {
        int(coord)
        for ft in FEATURES
        for iv in (feat_contig.get(ft, []),)
        if len(iv) > 0
        for pair in iv
        for coord in pair
    })
    midpoints = np.array(
        [(boundaries[i] + boundaries[i + 1]) // 2
         for i in range(len(boundaries) - 1)],
        dtype=np.int64)
    return (np.array(boundaries[:-1], dtype=np.int64),
            np.array(boundaries[1:], dtype=np.int64),
            classify(midpoints, feat_contig))


def read_feature_overlap(
    starts: np.ndarray, ends: np.ndarray,
    seg_starts: np.ndarray, seg_ends: np.ndarray, seg_labels: np.ndarray,
) -> dict[str, int]:
    """Sum base-pair overlap of reads with each feature type.

    *starts* and *ends* should be 0-based half-open.
    """
    feat_bp: dict[str, int] = {f: 0 for f in FEATURES}
    for i in range(len(starts)):
        rs, re = int(starts[i]), int(ends[i])
        idx = np.searchsorted(seg_ends, rs, side="right")
        while idx < len(seg_starts) and seg_starts[idx] < re:
            ov = min(re, int(seg_ends[idx])) - max(rs, int(seg_starts[idx]))
            if ov > 0:
                feat_bp[seg_labels[idx]] += ov
            idx += 1
    return feat_bp


# ====================================================================
# Cytoband (p/q arm) parsing
# ====================================================================

def parse_cytoband() -> dict[str, dict[str, np.ndarray]]:
    """Return ``{contig: {'p': Nx2, 'q': Nx2}}`` from the BED file."""
    raw: dict = defaultdict(lambda: {"p": [], "q": []})
    with open(CYTOBAND_BED) as fh:
        for line in fh:
            cols = line.strip().split("\t")
            contig = _gff_contig(cols[0])
            if "chrM" in contig:
                continue
            s, e, band = int(cols[1]), int(cols[2]), cols[3]
            if band.startswith("p"):
                raw[contig]["p"].append((s, e))
            elif band.startswith("q"):
                raw[contig]["q"].append((s, e))
    return {c: {"p": merge_intervals(raw[c]["p"]),
                "q": merge_intervals(raw[c]["q"])}
            for c in raw}


