"""
HG002 SSB Damage Analysis
=========================

Analyse biotin-labelled single-strand break (SSB) sites detected by
nanopore sequencing of the HG002 diploid human genome.  Two figures are
produced from per-read XLSX tables generated by ``biotin_ssb_pipeline.py``:

  1_genome_wide_SSB_density
      Side-by-side maternal/paternal karyogram coloured by SSB density
      per 1 Mb bin.

  2_gene_annotation_SSB_analysis
      Sequencing coverage and SSB density stratified by genomic feature
      (Promoter, 5'UTR, CDS, 3'UTR, Intron, Exon-ncRNA, Intergenic).

Coordinate conventions
----------------------
- XLSX read tables are 1-based inclusive ``[start, end]``.
- Reverse-strand coordinates are flipped to forward-strand coordinates
  via ``chrom_length + 1 - pos``.
- GFF annotations are converted internally to 0-based half-open
  ``[start, end)``.  When classifying 1-based SSB positions against
  these intervals the positions are shifted by ``-1``.
- mtDNA (chrM) is excluded throughout.

Usage
-----
::

    python hg002_analysis.py                 # both figures
    python hg002_analysis.py density         # genome-wide density only
    python hg002_analysis.py annotation      # gene annotation only

Dependencies: numpy, pandas, matplotlib, pysam, openpyxl
"""

