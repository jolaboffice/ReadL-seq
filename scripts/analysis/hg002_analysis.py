def compute_binned_density(dose: str,
                           bin_size: int = BIN_SIZE) -> pd.DataFrame:
    """Compute per-bin SSB density from per-read XLSX data.

    For each read the full ``[start, end]`` span contributes to
    ``base_coverage`` of overlapping bins, while ``biotin_hits`` is
    incremented only in the bin containing the 3' end of biotin-positive
    reads.  ``damage_per_Mb = biotin_hits / base_coverage * 1e6``.
    """
    chrom_lengths = get_chrom_lengths()
    bins: dict[tuple, dict] = {}

    def accumulate(df: pd.DataFrame, reverse: bool) -> None:
        for contig, grp in df.groupby("contig"):
            if contig not in chrom_lengths:
                continue
            clen = chrom_lengths[contig]
            for _, row in grp.iterrows():
                s, e = int(row["start"]), int(row["end"])
                if reverse:
                    s, e = clen + 1 - e, clen + 1 - s
                # Coverage: count overlapping bases per bin
                for b in range((s // bin_size) * bin_size,
                               (e // bin_size) * bin_size + bin_size,
                               bin_size):
                    rec = bins.setdefault((contig, b),
                                         {"base_coverage": 0,
                                          "biotin_hits": 0})
                    rec["base_coverage"] += (
                        min(e, b + bin_size - 1) - max(s, b) + 1)
                # SSB site: 3' end of biotin-positive reads
                if row["is_biotin"]:
                    end_bin = (e // bin_size) * bin_size
                    bins.setdefault((contig, end_bin),
                                   {"base_coverage": 0,
                                    "biotin_hits": 0})["biotin_hits"] += 1

    for strand, reverse in [("forward", False), ("reverse", True)]:
        key = f"{dose}_{strand}"
        print(f"  Loading {key} ...")
        df = load_xlsx(XLSX_PATHS[key])
        accumulate(df, reverse)
        print(f"    {len(df)} reads")

    rows = []
    for (contig, bin_start), rec in bins.items():
        chrom, *rest = contig.rsplit("_", 1)
        hap = rest[0] if rest else "unknown"
        bc, bh = rec["base_coverage"], rec["biotin_hits"]
        rows.append({
            "contig": contig, "chrom": chrom, "haplotype": hap,
            "bin_start": bin_start,
            "base_coverage": bc, "biotin_hits": bh,
            "damage_per_Mb": (bh / bc * 1e6) if bc > 0 else 0,
        })
    return pd.DataFrame(rows)


def _density_track(data: pd.DataFrame, chrom: str, max_bins: int,
                   bin_size: int = BIN_SIZE,
                   haplotype: str | None = None) -> np.ndarray:
    """Extract a 1-D density array for one chromosome (and haplotype)."""
    mask = data["chrom"] == chrom
    if haplotype is not None:
        mask &= data["haplotype"] == haplotype
    arr = np.full(max_bins, np.nan)
    for _, row in data.loc[mask].iterrows():
        idx = int(row["bin_start"]) // bin_size
        if idx < max_bins:
            arr[idx] = row["damage_per_Mb"]
    return arr


def plot_karyogram(data_0kgy: pd.DataFrame,
                   data_1kgy: pd.DataFrame,
                   bin_size: int = BIN_SIZE) -> plt.Figure:
    """Draw a side-by-side maternal | paternal karyogram.

    Each chromosome row has two tracks (1 kGy top, 0 kGy bottom) per
    haplotype.  chrX appears under maternal, chrY under paternal.
    """
    setup_plot_style()
    hap_sizes = get_hap_chrom_sizes(bin_size)

    # Determine chromosome widths (max of mat/pat)
    chrom_sizes: dict[str, int] = {}
    for chrom in CHROM_ORDER_NO_M:
        for hap in ("mat", "pat"):
            if (chrom, hap) in hap_sizes:
                chrom_sizes[chrom] = max(chrom_sizes.get(chrom, 0),
                                         hap_sizes[(chrom, hap)])

    # Rows: autosomes + combined X/Y row
    auto = [c for c in CHROM_ORDER_NO_M if c not in ("chrX", "chrY")]
    rows = [c for c in auto if c in chrom_sizes]
    if "chrX" in chrom_sizes or "chrY" in chrom_sizes:
        rows.append("chrX/Y")
        chrom_sizes["chrX/Y"] = max(chrom_sizes.get("chrX", 0),
                                     chrom_sizes.get("chrY", 0))

    n_rows = len(rows)
    max_size = max(chrom_sizes.values())
    max_bins = max_size // bin_size
    gap = 0.3  # vertical gap between 1 kGy and 0 kGy tracks

    fig, axes = plt.subplots(
        n_rows, 2, figsize=(16, n_rows * 0.5 + 2),
        gridspec_kw={"hspace": 0.15, "wspace": 0.05})
    if n_rows == 1:
        axes = axes[np.newaxis, :]

    # Colour scale: 95th percentile of 1 kGy
    vals_1k = data_1kgy["damage_per_Mb"].dropna()
    vmax = (np.percentile(vals_1k[vals_1k > 0], 95)
            if len(vals_1k[vals_1k > 0]) > 0 else 10)
    norm = Normalize(vmin=0, vmax=vmax, clip=True)
    cmap = plt.cm.YlOrRd.copy()
    cmap.set_bad("white")

    for ri, row_label in enumerate(rows):
        for ci, hap in enumerate(("mat", "pat")):
            ax = axes[ri, ci]
            chrom = (("chrX" if hap == "mat" else "chrY")
                     if row_label == "chrX/Y" else row_label)

            t1 = _density_track(data_1kgy, chrom, max_bins, bin_size, hap)
            t0 = _density_track(data_0kgy, chrom, max_bins, bin_size, hap)

            if np.all(np.isnan(t1)):
                ax.set_visible(False)
                continue

            ref = hap_sizes.get((chrom, hap),
                                chrom_sizes.get(chrom, max_size))
            ax.imshow(t1[np.newaxis, :], aspect="auto", cmap=cmap,
                      norm=norm, extent=[0, max_size, 1 + gap, 2 + gap],
                      interpolation="nearest")
            ax.imshow(t0[np.newaxis, :], aspect="auto", cmap=cmap,
                      norm=norm, extent=[0, max_size, 0, 1],
                      interpolation="nearest")
            for y0 in (1 + gap, 0):
                ax.add_patch(mpatches.Rectangle(
                    (0, y0), ref, 1,
                    facecolor="none", edgecolor="black", linewidth=0.5))
            ax.set(xlim=(0, max_size), ylim=(-0.1, 2 + gap + 0.1))
            ax.set_yticks([])
            ax.set_xticks([])
            for sp in ax.spines.values():
                sp.set_visible(False)

        # Chromosome label
        ymid = (2 + gap) / 2
        if row_label == "chrX/Y":
            axes[ri, 0].text(-max_size * 0.02, ymid, "X", ha="right",
                             va="center", fontsize=9, fontweight="bold")
            axes[ri, 1].text(max_size * 1.02, ymid, "Y", ha="left",
                             va="center", fontsize=9, fontweight="bold")
        else:
            axes[ri, 0].text(-max_size * 0.02, ymid,
                             row_label.replace("chr", ""), ha="right",
                             va="center", fontsize=9, fontweight="bold")

    axes[0, 0].set_title("maternal", fontsize=11, fontweight="bold", pad=8)
    axes[0, 1].set_title("paternal", fontsize=11, fontweight="bold", pad=8)

    # Scale bar
    sb = 50_000_000
    axes[-1, 0].plot([0, sb], [-0.5, -0.5], "k-", lw=1.5, clip_on=False)
    axes[-1, 0].text(sb / 2, -0.8, "50 Mb", ha="center", va="top",
                     fontsize=8, clip_on=False)

    # Colour bar
    sm = ScalarMappable(cmap=cmap, norm=norm)
    sm.set_array([])
    cb_ax = fig.add_axes([0.92, 0.3, 0.012, 0.4])
    cb = fig.colorbar(sm, cax=cb_ax)
    cb.set_label("SSB density\n(per Mb)", fontsize=9)
    cb.ax.tick_params(labelsize=8)

    fig.suptitle("Genome-wide SSB Damage Distribution",
                 fontsize=14, fontweight="bold", y=0.98)
    return fig


def generate_genome_wide_density() -> None:
    """End-to-end: compute binned density and save karyogram."""
    print("=== Genome-wide SSB density ===")
    bins_0 = compute_binned_density("0kGy")
    bins_1 = compute_binned_density("1kGy")

    combined = pd.concat([bins_0.assign(dose="0kGy"),
                          bins_1.assign(dose="1kGy")], ignore_index=True)
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    for ext, method in [("csv", "to_csv"), ("xlsx", "to_excel")]:
        getattr(combined, method)(
            os.path.join(OUTPUT_DIR,
                         f"1_genome_wide_SSB_density_data.{ext}"),
            index=False)

    fig = plot_karyogram(bins_0, bins_1)
    save_figure(fig, "1_genome_wide_SSB_density")
    print("Done.\n")


