def get_chrom_lengths() -> dict[str, int]:
    """Return ``{contig_name: length}`` from the reference FASTA index."""
    import pysam
    with pysam.FastaFile(REFERENCE_FASTA) as fa:
        return {name: fa.get_reference_length(name)
                for name in fa.references}


def get_hap_chrom_sizes(bin_size: int = BIN_SIZE) -> dict[tuple, int]:
    """Return ``{(chrom, hap): size}`` rounded up to *bin_size*."""
    sizes = {}
    for contig, length in get_chrom_lengths().items():
        parts = contig.rsplit("_", 1)
        if len(parts) == 2:
            sizes[(parts[0], parts[1])] = math.ceil(length / bin_size) * bin_size
    return sizes


