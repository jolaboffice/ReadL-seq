def process_dose(
    dose: str,
    feat: dict[str, dict],
    chrom_lengths: dict[str, int],
    segments: dict[str, tuple],
) -> tuple[dict, dict, dict]:
    """Classify reads and SSB sites by genomic feature for one dose.

    Returns ``(feat_bp, feat_biotin, ssb_positions)`` where:

    - *feat_bp*: total sequenced base-pairs per feature (denominator).
    - *feat_biotin*: biotin-positive read count per feature (numerator).
    - *ssb_positions*: ``{contig: sorted int64 array}`` of SSB 3' ends.
    """
    feat_bp: dict[str, int] = {f: 0 for f in FEATURES}
    feat_biotin: dict[str, int] = {f: 0 for f in FEATURES}
    ssb_pos: dict[str, list] = defaultdict(list)

    for strand in ("forward", "reverse"):
        key = f"{dose}_{strand}"
        print(f"  Loading {key} ...")
        df = pd.read_excel(XLSX_PATHS[key])
        df = df[~df["contig"].str.contains("chrM")]

        for contig, grp in df.groupby("contig"):
            if contig not in segments:
                continue
            seg_s, seg_e, seg_l = segments[contig]
            clen = chrom_lengths[contig]

            starts = grp["start"].values.astype(np.int64)
            ends = grp["end"].values.astype(np.int64)
            is_bio = grp["is_biotin"].values.astype(bool)

            if strand == "reverse":
                starts, ends = clen + 1 - ends, clen + 1 - starts
                bio_ends = starts[is_bio]      # 3' end = new start
            else:
                bio_ends = ends[is_bio]         # 3' end = end

            # Denominator: read-feature overlap
            # 1-based [start,end] -> 0-based [start-1, end)
            bp = read_feature_overlap(starts - 1, ends, seg_s, seg_e, seg_l)
            for f in FEATURES:
                feat_bp[f] += bp[f]

            # Numerator: classify biotin 3' ends (1-based -> 0-based)
            if len(bio_ends) > 0:
                bio_sorted = np.sort(bio_ends)
                labels = classify(bio_sorted - 1, feat[contig])
                for f in FEATURES:
                    feat_biotin[f] += int(np.sum(labels == f))
                ssb_pos[contig].append(bio_sorted)

            print(f"    {contig}: {len(grp)} reads")

    merged = {c: np.sort(np.concatenate(arrs))
              for c, arrs in ssb_pos.items()}
    return feat_bp, feat_biotin, merged


def plot_annotation(genome_size: dict[str, int],
                    results: dict) -> plt.Figure:
    """Draw two-panel figure: coverage and SSB density by genomic feature.

    Panel A — horizontal bars showing sequencing coverage (x) per feature.
    Panel B — horizontal bars showing SSB density (SSB per Mb) per feature.
    """
    setup_plot_style()

    fig = plt.figure(figsize=(10, 4))
    gs = GridSpec(1, 2, width_ratios=[1, 1.2], wspace=0.4,
                  left=0.10, right=0.95, bottom=0.18, top=0.86)
    width = 0.35

    # Sort features by 1 kGy SSB density (descending)
    dens_1 = {f: results["1kGy"]["density"][f] * 1e6 for f in FEATURES}
    order = sorted(FEATURES, key=lambda f: dens_1[f], reverse=True)
    y = np.arange(len(order))

    # -- Panel A: Sequencing coverage --
    ax_a = fig.add_subplot(gs[0])
    cov = {}
    for dose in ("0kGy", "1kGy"):
        cov[dose] = [results[dose]["bp"][f] / genome_size[f]
                     if genome_size[f] > 0 else 0 for f in order]
    mb = {dose: [results[dose]["bp"][f] / 1e6 for f in order]
          for dose in ("0kGy", "1kGy")}
    colors = [FEATURE_COLORS[f] for f in order]

    ax_a.barh(y - width / 2, cov["1kGy"], height=width,
              color=colors, edgecolor="black", linewidth=0.5, alpha=0.9)
    ax_a.barh(y + width / 2, cov["0kGy"], height=width,
              color=colors, edgecolor="black", linewidth=0.5, alpha=0.5)
    ax_a.set_yticks(y)
    ax_a.set_yticklabels(order, fontsize=10)
    ax_a.set_xlabel("Sequencing coverage (x)", fontsize=10)
    ax_a.set_title("A. Coverage by genomic feature",
                    fontweight="bold", loc="left", fontsize=11)
    ax_a.invert_yaxis()

    mb_x = min(min(cov["1kGy"]), min(cov["0kGy"])) * 0.5
    for i in range(len(order)):
        for dose, dy, alpha in [("1kGy", -width / 2, 0.9),
                                ("0kGy", +width / 2, 0.5)]:
            ax_a.text(cov[dose][i] * 1.02, i + dy,
                      f"{cov[dose][i]:.2f}x",
                      va="center", ha="left", fontsize=8)
            ax_a.text(mb_x, i + dy, f"{mb[dose][i]:.0f} Mb",
                      va="center", ha="center", fontsize=7,
                      fontweight="bold")
    ax_a.set_xlim(0, max(cov["1kGy"]) * 1.25)

    # -- Panel B: SSB density --
    ax_b = fig.add_subplot(gs[1])
    dens = {dose: [results[dose]["density"][f] * 1e6 for f in order]
            for dose in ("0kGy", "1kGy")}

    ax_b.barh(y - width / 2, dens["1kGy"], height=width,
              color=colors, edgecolor="black", linewidth=0.5, alpha=0.9)
    ax_b.barh(y + width / 2, dens["0kGy"], height=width,
              color=colors, edgecolor="black", linewidth=0.5, alpha=0.5)
    ax_b.set_yticks(y)
    ax_b.set_yticklabels(order, fontsize=10)
    ax_b.set_xlabel("SSB density (SSB per Mb)", fontsize=10)
    ax_b.set_title("B. SSB density by feature",
                    fontweight="bold", loc="left", fontsize=11)
    ax_b.invert_yaxis()
    ax_b.legend(handles=[
        Patch(facecolor="gray", edgecolor="black", alpha=0.9, label="1 kGy"),
        Patch(facecolor="gray", edgecolor="black", alpha=0.5, label="0 kGy"),
    ], fontsize=9, frameon=False)

    for i in range(len(order)):
        for dose, dy in [("1kGy", -width / 2), ("0kGy", +width / 2)]:
            ax_b.text(dens[dose][i] * 1.02, i + dy,
                      f"{dens[dose][i]:.1f}",
                      va="center", ha="left", fontsize=8)
    ax_b.set_xlim(0, max(dens["1kGy"]) * 1.25)

    fig.suptitle("SSB Damage Distribution by Genomic Feature",
                 fontsize=13, fontweight="bold")
    return fig


def generate_annotation_analysis() -> None:
    """End-to-end: parse annotations, quantify SSBs, and save figure."""
    print("=== Gene annotation SSB analysis ===")

    # Parse GFF annotations
    feat: dict = {}
    for path in GFF_FILES.values():
        feat.update(parse_gff(path))

    chrom_lengths = {k: v for k, v in get_chrom_lengths().items()
                     if "chrM" not in k}
    feat = {k: v for k, v in feat.items() if "chrM" not in k}

    # Build resolved segments
    print("\nBuilding resolved segments ...")
    segments = {}
    for contig, fdict in feat.items():
        clen = chrom_lengths.get(contig)
        if clen is not None:
            segments[contig] = build_resolved_segments(fdict, clen)

    # Genome feature sizes
    print("\nGenome feature sizes:")
    genome_size: dict[str, int] = {f: 0 for f in FEATURES}
    total = sum(chrom_lengths[c] for c in chrom_lengths if c in feat)
    for contig in feat:
        if contig not in segments:
            continue
        ss, se, sl = segments[contig]
        for i in range(len(ss)):
            genome_size[sl[i]] += int(se[i] - ss[i])
    for f in FEATURES:
        print(f"  {f:<16}: {genome_size[f]:>14,} bp "
              f"({genome_size[f] / total * 100:.2f}%)")

    # Process each dose
    results: dict = {}
    all_ssb: dict = {}
    for dose in ("0kGy", "1kGy"):
        print(f"\n=== {dose} ===")
        bp, bio, ssb = process_dose(dose, feat, chrom_lengths, segments)
        dens = {f: bio[f] / bp[f] if bp[f] > 0 else 0 for f in FEATURES}
        results[dose] = {"bp": bp, "biotin": bio, "density": dens}
        all_ssb[dose] = ssb

        print(f"\n  {'Feature':<16} | {'sequenced_bp':>14} | "
              f"{'biotin_reads':>12} | {'SSB/bp':>12}")
        print("  " + "-" * 58)
        for f in FEATURES:
            print(f"  {f:<16} | {bp[f]:>14,} | "
                  f"{bio[f]:>12,} | {dens[f]:>12.4e}")

    # p/q arm analysis (1 kGy)
    print("\n=== p-arm vs q-arm (1 kGy) ===")
    arms = parse_cytoband()
    arm_rows = []
    for contig in sorted(all_ssb["1kGy"]):
        positions = all_ssb["1kGy"][contig]
        if contig not in arms or len(positions) == 0:
            continue
        for arm in ("p", "q"):
            ivs = arms[contig][arm]
            if len(ivs) == 0:
                continue
            n = int(np.sum(pos_in_intervals(positions - 1, ivs)))
            size_mb = intervals_bases(ivs) / 1e6
            chrom, hap = contig.rsplit("_", 1)
            arm_rows.append({
                "contig": contig, "chrom": chrom, "haplotype": hap,
                "arm": arm, "ssb_count": n,
                "arm_size_mb": size_mb,
                "density": n / size_mb if size_mb > 0 else 0,
            })
    arm_df = pd.DataFrame(arm_rows)
    print(arm_df.groupby("arm")[["ssb_count", "density"]].mean())

    # Save data
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    base = "2_gene_annotation_SSB_analysis"

    coverage = {
        dose: {f: results[dose]["bp"][f] / genome_size[f]
               if genome_size[f] > 0 else 0 for f in FEATURES}
        for dose in ("0kGy", "1kGy")}
    feat_rows = []
    for f in FEATURES:
        row: dict = {"feature": f, "genome_size_bp": genome_size[f]}
        for dose in ("0kGy", "1kGy"):
            row[f"{dose}_sequenced_bp"] = results[dose]["bp"][f]
            row[f"{dose}_coverage"] = coverage[dose][f]
            row[f"{dose}_biotin_reads"] = results[dose]["biotin"][f]
            row[f"{dose}_SSB_per_bp"] = results[dose]["density"][f]
            row[f"{dose}_SSB_per_Mb"] = results[dose]["density"][f] * 1e6
        feat_rows.append(row)

    feat_df = pd.DataFrame(feat_rows)
    feat_df.to_csv(os.path.join(OUTPUT_DIR, f"{base}_data.csv"), index=False)
    feat_df.to_excel(os.path.join(OUTPUT_DIR, f"{base}_data.xlsx"),
                     index=False)
    arm_df.to_csv(os.path.join(OUTPUT_DIR, f"{base}_arms.csv"), index=False)
    arm_df.to_excel(os.path.join(OUTPUT_DIR, f"{base}_arms.xlsx"),
                    index=False)
    print("\n  Data saved.")

    fig = plot_annotation(genome_size, results)
    save_figure(fig, base)
    print("Done.\n")


